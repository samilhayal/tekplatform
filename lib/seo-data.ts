/**
 * Generate optimized title for tool pages
 */
export function generateToolTitle(toolName: string, keywords: string[]): string {
  const year = new Date().getFullYear()
  return `${toolName} - ${keywords.slice(0, 2).join(', ')} | Ücretsiz Online Araç ${year}`
}

/**
 * Generate optimized meta description
 */
export function generateMetaDescription(toolName: string, description: string, keywords: string[]): string {
  return `${toolName}: ${description} ✓ ${keywords.slice(0, 3).join(' ✓ ')} ✓ Ücretsiz ✓ Hızlı ✓ Kolay kullanım ✓ Reklamsız ✓ Online hesaplama aracı.`
}

