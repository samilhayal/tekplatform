// Persentil kategorileri
export type PersentilKategori = 'cok-dusuk' | 'dusuk' | 'normal' | 'yuksek' | 'cok-yuksek';

export interface PersentilSonuc {
  deger: number;
  zScore: number;
  persentil: number;
  kategori: PersentilKategori;
  kategoriLabel: string;
  kategoriRenk: string;
}

// Z-score hesaplama fonksiyonu (WHO LMS yöntemi)
export function hesaplaZScore(X: number, L: number, M: number, S: number): number {
  if (L === 0) {
    return Math.log(X / M) / S;
  } else {
    return (Math.pow(X / M, L) - 1) / (L * S);
  }
}

// Z-score'dan persentil hesaplama (normal dağılım CDF)
export function zScoreToPersentil(z: number): number {
  // Error function approximation
  const erf = (x: number): number => {
    const a1 = 0.254829592;
    const a2 = -0.284496736;
    const a3 = 1.421413741;
    const a4 = -1.453152027;
    const a5 = 1.061405429;
    const p = 0.3275911;

    const sign = x >= 0 ? 1 : -1;
    x = Math.abs(x);

    const t = 1.0 / (1.0 + p * x);
    const y = 1.0 - (((((a5 * t + a4) * t) + a3) * t + a2) * t + a1) * t * Math.exp(-x * x);

    return sign * y;
  };

  return 0.5 * (1 + erf(z / Math.sqrt(2))) * 100;
}

// Persentil kategorisini belirle
export function persentilKategorisi(persentil: number): { kategori: PersentilKategori; label: string; renk: string } {
  if (persentil <= 3) {
    return { kategori: 'cok-dusuk', label: 'Çok Düşük', renk: 'red' };
  } else if (persentil <= 15) {
    return { kategori: 'dusuk', label: 'Düşük', renk: 'orange' };
  } else if (persentil <= 85) {
    return { kategori: 'normal', label: 'Normal', renk: 'green' };
  } else if (persentil <= 97) {
    return { kategori: 'yuksek', label: 'Yüksek', renk: 'blue' };
  } else {
    return { kategori: 'cok-yuksek', label: 'Çok Yüksek', renk: 'purple' };
  }
}

// LMS değerlerini al
export function getLMSValues(
  olcumTipi: 'boy' | 'kilo' | 'basCevresi',
  cinsiyet: 'erkek' | 'kiz',
  ayOlarak: number
): { L: number; M: number; S: number } | null {
  // Ay değerini sınırla (0-60)
  const ay = Math.max(0, Math.min(60, Math.round(ayOlarak)));
  
  let lmsTable: { [ay: number]: { L: number; M: number; S: number } };
  
  if (olcumTipi === 'boy') {
    lmsTable = cinsiyet === 'erkek' ? boyErkekLMS : boyKizLMS;
  } else if (olcumTipi === 'kilo') {
    lmsTable = cinsiyet === 'erkek' ? kiloErkekLMS : kiloKizLMS;
  } else {
    lmsTable = cinsiyet === 'erkek' ? basCevresiErkekLMS : basCevresiKizLMS;
  }
  
  return lmsTable[ay] || null;
}

// Tam persentil hesaplama
export function hesaplaPersentil(
  olcumTipi: 'boy' | 'kilo' | 'basCevresi',
  cinsiyet: 'erkek' | 'kiz',
  ayOlarak: number,
  olcum: number
): PersentilSonuc | null {
  const lms = getLMSValues(olcumTipi, cinsiyet, ayOlarak);
  
  if (!lms) return null;
  
  const zScore = hesaplaZScore(olcum, lms.L, lms.M, lms.S);
  const persentil = zScoreToPersentil(zScore);
  const { kategori, label, renk } = persentilKategorisi(persentil);
  
  return {
    deger: olcum,
    zScore: Math.round(zScore * 100) / 100,
    persentil: Math.round(persentil * 10) / 10,
    kategori,
    kategoriLabel: label,
    kategoriRenk: renk
  };
}

// Persentil eğrisi için veri oluştur
export function persentilEgriVerisi(
  olcumTipi: 'boy' | 'kilo' | 'basCevresi',
  cinsiyet: 'erkek' | 'kiz',
  persentilDeger: number // 3, 15, 50, 85, 97
): { ay: number; deger: number }[] {
  const sonuc: { ay: number; deger: number }[] = [];
  
  // Z-score for percentile
  const zForPersentil = (p: number): number => {
    // Inverse normal CDF approximation
    const a = [
      -3.969683028665376e+01,
      2.209460984245205e+02,
      -2.759285104469687e+02,
      1.383577518672690e+02,
      -3.066479806614716e+01,
      2.506628277459239e+00
    ];
    const b = [
      -5.447609879822406e+01,
      1.615858368580409e+02,
      -1.556989798598866e+02,
      6.680131188771972e+01,
      -1.328068155288572e+01
    ];
    const c = [
      -7.784894002430293e-03,
      -3.223964580411365e-01,
      -2.400758277161838e+00,
      -2.549732539343734e+00,
      4.374664141464968e+00,
      2.938163982698783e+00
    ];
    const d = [
      7.784695709041462e-03,
      3.224671290700398e-01,
      2.445134137142996e+00,
      3.754408661907416e+00
    ];

    const pLow = 0.02425;
    const pHigh = 1 - pLow;
    let q, r;

    if (p < pLow) {
      q = Math.sqrt(-2 * Math.log(p));
      return (((((c[0] * q + c[1]) * q + c[2]) * q + c[3]) * q + c[4]) * q + c[5]) /
        ((((d[0] * q + d[1]) * q + d[2]) * q + d[3]) * q + 1);
    } else if (p <= pHigh) {
      q = p - 0.5;
      r = q * q;
      return (((((a[0] * r + a[1]) * r + a[2]) * r + a[3]) * r + a[4]) * r + a[5]) * q /
        (((((b[0] * r + b[1]) * r + b[2]) * r + b[3]) * r + b[4]) * r + 1);
    } else {
      q = Math.sqrt(-2 * Math.log(1 - p));
      return -(((((c[0] * q + c[1]) * q + c[2]) * q + c[3]) * q + c[4]) * q + c[5]) /
        ((((d[0] * q + d[1]) * q + d[2]) * q + d[3]) * q + 1);
    }
  };

  const z = zForPersentil(persentilDeger / 100);

  for (let ay = 0; ay <= 60; ay++) {
    const lms = getLMSValues(olcumTipi, cinsiyet, ay);
    if (lms) {
      // X = M * (1 + L * S * Z)^(1/L) for L != 0
      // X = M * exp(S * Z) for L == 0
      let deger: number;
      if (lms.L === 0) {
        deger = lms.M * Math.exp(lms.S * z);
      } else {
        deger = lms.M * Math.pow(1 + lms.L * lms.S * z, 1 / lms.L);
      }
      sonuc.push({ ay, deger: Math.round(deger * 10) / 10 });
    }
  }

  return sonuc;
}

// Yaş hesaplama (doğum tarihinden)
export function yasHesapla(dogumTarihi: Date): { yil: number; ay: number; gun: number; toplamAy: number } {
  const bugun = new Date();
  
  let yil = bugun.getFullYear() - dogumTarihi.getFullYear();
  let ay = bugun.getMonth() - dogumTarihi.getMonth();
  let gun = bugun.getDate() - dogumTarihi.getDate();

  if (gun < 0) {
    ay--;
    const oncekiAy = new Date(bugun.getFullYear(), bugun.getMonth(), 0);
    gun += oncekiAy.getDate();
  }

  if (ay < 0) {
    yil--;
    ay += 12;
  }

  const toplamAy = yil * 12 + ay + (gun / 30);

  return { yil, ay, gun, toplamAy };
}
